<h1>Spelregels voor Bolknoms</h1>
<p>
    Voor het gebruik van Bolknoms en het mee-eten aan de eettafel gelden een paar spelregels. Daarnaast is het gebruik van deze website gebonden aan de <a href="/disclaimer">disclaimer</a>. De gegevens die van jou bekend zijn bij De Bolk, zijn onderworpen aan de regels in de <a href="/privacy">privacyverklaring</a>.
</p>

<h2>Aanmelden</h2>
<ol>
    <li>Je kunt niet mee-eten zonder je aan te melden.</li>
    <li>Je kunt je aanmelden tot de vastgestelde sluitingstijd (meestal 15:00 uur). De sluitingstijd wordt bepaald door het bestuur en staat vermeld bij de maaltijd.</li>
    <li>Externen kunnen zich aanmelden voor de eerstvolgende maaltijd. Bolkers kunnen zich aanmelden voor alle open maaltijden.</li>
    <li>Je kunt je weer afmelden tot aan de sluitingstijd. Als je met je Bolkaccount aangemeld bent, doe je
    dat via de website. Als je geen Bolkaccount hebt, meld je je af door het bestuur te bellen op <a href="tel:+31152126012">015 212 60 12</a>.</li>
</ol>

<h2>Mee-eten</h2>
<ol>
    <li>De maaltijd begint om 18:30 uur. Het kan zijn dat bij uitzondering de maaltijd op een ander moment begint; dat wordt vermeld bij de maaltijd.</li>
    <li>Als je te laat komt, loop je het risico dat er geen eten meer voor je is. Je moet in dat geval wel voor de maaltijd betalen.</li>
</ol>

<h2>Betalen</h2>
<ol>
    <li>Je betaalt de maaltijd met een bonnetje. Die kun je kopen in de soci&euml;teit.</li>
    <li>Als je niet op tijd afmeldt en niet komt opdagen, moet je wel voor de maaltijd betalen (meestal &euro;3,50). Er is immers wel voor je gekookt. Voor leden gaat dat op Bolkrekening, externen krijgen een factuur gemaild.</li>
    <li>Bolkers zijn verantwoordelijk voor de betaling van de +1's die ze aanmelden.</li>
    <li>Als je de kosten van de gemiste maaltijd niet betaalt, kun je niet meer mee-eten aan de eettafel.</li>
</ol>

<h1>Voordeel van je Bolkaccount</h1>
<p>
    Aanmelden voor maaltijden met je Bolkaccount bij Bolknoms heeft enkele voordelen.
</p>

<h2>Kan ik mij registreren voor een  Bolkaccount?</h2>
<p>
    Nee, helaas niet. Je komt alleen in aanmerking voor een Bolkaccount als je kandidaat-lid, gewoon lid of oud-lid bent. Het is overigens onmogelijk om in de ledenadministratie vermeld te staan zonder een Bolkaccount te hebben, dus alle kandidaat-leden, gewone leden en oud-leden hebben al een Bolkaccount.
</p>

<h2>Aanmelden zonder Bolkaccount</h2>
<ol>
    <li>Je kunt je alleen maar aanmelden voor de eerstvolgende maaltijd.</li>
    <li>Direct na je aanmelding ontvang je een e-mail. Je moet je aanmelding bevestigen door op de link in die e-mail te klikken.</li>
    <li>Ook als je niet komt opdagen, is er natuurlijk wel voor je gekookt. De kosten van de maaltijd worden dan aan je gefactureerd. Betaal je die factuur niet, dan wordt je e-mailadres geblokkeerd en kun je niet meer mee-eten aan de eettafel.</li>
</ol>

<h2>Aanmelden met Bolkaccount</h2>
<ol>
    <li>Je hoeft niet per e-mail te bevestigen.</li>
    <li>Je komt in de lijst met mensen die het meest mee-eten.</li>
    <li>Je kunt je in één keer aanmelden voor meerdere dagen.</li>
    <li>Je kunt een abonnement instellen waardoor je automatisch wordt aangemeld.</li>
    <li>Je kunt externen makkelijk meenemen, zonder dat ze zich apart hoeven aan te melden.</li>
</ol>
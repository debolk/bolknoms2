<h3>Toegang geweigerd</h3>
<p>
    Je hebt geweigerd om de gebruikersnaam van je Bolkaccount met Bolknoms te delen. Dat is onhandig, want nu weten we niet wie je bent en mag je niet naar binnen. Als je geen lid bent, heb je geen Bolkaccount. Je kunt je dan <a href="/geenaccount">aanmelden zonder Bolkaccount</a>.
</p>
<p>
    Bolknoms gebruikt alleen je gebruikersnaam om te bepalen wie je bent en of je bestuur bent. Met de rest van je Bolk-account doen we verder niets.
</p>
